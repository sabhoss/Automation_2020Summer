package Action_itemz;

public class action_item {

    public static void main(String[] args) {

        String[] city = new String[4];
        city[0] = "Brooklyn";
        city[1] = "Manhattan";
        city[2] = "Staten Island";
        city[3] = "Queens";

        for (int i = 0; 1 < city.length; i++) {

            if (city[i] == "Brooklyn") {
                System.out.println("My city is " + city[i]);
            } else if (city[i] == "Manhattan") {
                System.out.println("My city is " + city[i]);
            }//end of conditional statement

        }//end of for loop


    }//end of main method

}//end of java class